package com.newsoperators;

public class MaxMinExponentialNotation {
    public static void main(String[] args) {
        float expFloat = 1.39E-43f;
        System.out.println("Values for floats: ");
        System.out.println("MAX EXPONENTIAL VALUE: "+Float.MAX_EXPONENT);
        System.out.println("MAX VALUE: "+Float.MAX_VALUE);
        System.out.println("MIN EXPONENTIAL VALUE: "+Float.MIN_EXPONENT);

        System.out.println("Values for double: ");
        System.out.println("MAX EXPONENTIAL VALUE: "+Double.MAX_EXPONENT);
        System.out.println("MAX VALUE: "+Double.MAX_VALUE);
        System.out.println("MIN EXPONENTIAL VALUE: "+Double.MIN_EXPONENT);
    }

}
